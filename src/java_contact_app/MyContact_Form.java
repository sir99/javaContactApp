    private void showData(int index) {
        
        // get selected row index
        jTextField_id.setText(jTable1.getValueAt(index, 0).toString());
        jTextField_fullname.setText(jTable1.getValueAt(index, 1).toString());
        jComboBox_group.setSelectedItem(jTable1.getValueAt(index, 2).toString());
        jTextField_phone.setText(jTable1.getValueAt(index, 3).toString());
        jTextField_email.setText(jTable1.getValueAt(index, 4).toString());
        jTextArea_address.setText(jTable1.getValueAt(index, 5).toString());
        Image pic = ((ImageIcon) jTable1.getValueAt(index, 6)).getImage().getScaledInstance(jLabelContact_picture.getWidth(), jLabelContact_picture.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon img = new ImageIcon(pic);
        jLabelContact_picture.setIcon(img);
        
    }
    
    private void jButton_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_editActionPerformed
        
        // the user must select a contact to delete
        if(!jTextField_id.getText().equals("") || imagePth != null) {
            int id = Integer.valueOf(jTextField_id.getText());
            String fname = jTextField_fullname.getText();
            String phone = jTextField_phone.getText();
            String address = jTextArea_address.getText();
            String email = jTextField_email.getText();
            String groupc = jComboBox_group.getSelectedItem().toString();
            byte[] img = null;

            // if the user want to update data and image
            if(imagePth != null) {
                try {
                    Path pth = Paths.get(imagePth);
                    img = Files.readAllBytes(pth);
                    Contact c = new Contact(id, fname, groupc, phone, email, address, img, currentUserId);

                    PreparedStatement statement = null;
                    ContactQuery cq = new ContactQuery();
                    try {
                        cq.updateContact(c, true);
                        refreshJtable();
                        clearFields();
                    } catch (SQLException ex) {
                        Logger.getLogger(MyContact_Form.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(MyContact_Form.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(MyContact_Form.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                Contact c = new Contact(id, fname, groupc, phone, email, address, null, currentUserId);
                PreparedStatement statement = null;
                ContactQuery cq = new ContactQuery();
                try {
                    cq.updateContact(c, false);
                    refreshJtable();
                    clearFields();
                } catch (SQLException ex) {
                    Logger.getLogger(MyContact_Form.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Select a contact from a table");
        }
        
    }//GEN-LAST:event_jButton_editActionPerformed

    private void jButton_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_deleteActionPerformed
        
        // the user must select a contact to delete
        if(!jTextField_id.getText().equals("")) {
            int id = Integer.valueOf(jTextField_id.getText());
            ContactQuery cq = new ContactQuery();
            try {
                cq.deleteContact(id);
                refreshJtable();
                clearFields();
            } catch (SQLException ex) {
                Logger.getLogger(MyContact_Form.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Select a contact from a table");
        }
        
    }//GEN-LAST:event_jButton_deleteActionPerformed

