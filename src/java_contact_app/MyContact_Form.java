    public void populateJtable() throws SQLException {
        
        ContactQuery cq = new ContactQuery();
        ArrayList<Contact> clist = cq.contactList(currentUserId);
        
        String[] colNames = {"Id", "Fullname", "Group", "Phone", "Email", "Address", "Image"};
        Object[][] rows = new Object[clist.size()][8];
        
        for(int i=0; i<clist.size(); i++) {
            rows[i][0] = clist.get(i).getCid();
            rows[i][1] = clist.get(i).getFullname();
            rows[i][2] = clist.get(i).getGroupc();
            rows[i][3] = clist.get(i).getPhone();
            rows[i][4] = clist.get(i).getEmail();
            rows[i][5] = clist.get(i).getAddress();
            
            ImageIcon pic = new ImageIcon(new ImageIcon(clist.get(i).getPic()).getImage().getScaledInstance(120, 80, Image.SCALE_SMOOTH));
            rows[i][6] = pic;
            
        }
        
        MyModel md = new MyModel(rows, colNames);
        jTable1.setModel(md);
        jTable1.setRowHeight(80);
        jTable1.getColumnModel().getColumn(5).setPreferredWidth(120);
        
    }
    
